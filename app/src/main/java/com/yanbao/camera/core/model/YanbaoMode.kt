package com.yanbao.camera.core.model

/**
 * 雁宝 AI 相机的 9 大核心模式
 * 
 * 这是整个 App 的灵魂，每个模式对应不同的交互逻辑和参数面板
 */
enum class YanbaoMode(val displayName: String, val description: String) {
    /**
     * 雁宝记忆：查看历史拍摄的照片和参数
     */
    MEMORY("雁宝记忆", "查看您的专属记忆"),
    
    /**
     * 大师模式：预设的专业参数组合
     */
    MASTER("大师", "专业摄影师预设"),
    
    /**
     * 一键美颜：智能美颜算法
     */
    BEAUTY("一键美颜", "智能美颜"),
    
    /**
     * 29D 模式：29 个专业参数的完全控制
     */
    D29("29D", "29 个专业参数"),
    
    /**
     * 2.9D 模式：简化版的 29D，只保留核心参数
     */
    D2_9("2.9D", "简化版 29D"),
    
    /**
     * 原相机：iPhone 级别的原生相机功能
     */
    PRO("原相机", "iPhone 级原生功能"),
    
    /**
     * 基本功能：最简单的拍照模式
     */
    BASIC("基本功能", "简单拍照"),
    
    /**
     * AR 空间：增强现实拍摄
     */
    AR("AR空间", "增强现实"),
    
    /**
     * 相册：查看所有照片
     */
    GALLERY("相册", "查看照片")
}
