package com.yanbao.camera.core.model

/**
 * 雁宝 AI 相机的 9 大核心模式
 * 
 * 这是整个 App 的灵魂，每个模式对应不同的交互逻辑和参数面板
 */
/**
 * 雁宝 AI 相机的9 大核心模式（按设计图顺序）
 *
 * 顺序：雁宝记忆 → 大师 → 29D → 2.9D → 美颜 → 视频大师 → 基本 → 原相机 → AR
 */
enum class YanbaoMode(val displayName: String, val description: String) {

    /** 雁宝记忆：记录并复用历史拍摄参数 */
    MEMORY("雁宝记忆", "查看您的专属记忆"),

    /** 大师模式：专业摄影师预设国家轮盘 */
    MASTER("大师", "专业摄影师预设"),

    /** 29D 模式：29 个专业参数的完全控制（四 Tab 滑块面板）*/
    D29("29D", "29 个专业参数"),

    /** 2.9D 模式：简化版 29D，只保留核心参数 */
    D2_9("2.9D", "简化版 29D"),

    /** 美颜模式：智能美颜算法 */
    BEAUTY("美颜", "智能美颜"),

    /** 视频大师：专业视频录制模式 */
    VIDEO_MASTER("视频大师", "专业视频录制"),

    /** 基本模式：最简单的拍照模式 */
    BASIC("基本", "简单拍照"),

    /** 原相机：Camera2 API 专业控制 */
    PRO("原相机", "iPhone 级原生功能"),

    /** AR 空间：增强现实拍摄 */
    AR("AR", "增强现实")
}
